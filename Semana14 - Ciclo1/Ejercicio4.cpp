void Ventana()
{
	Console::SetWindowSize(ANCHO_CONSOLA, ALTO_CONSOLA);
	Console::CursorVisible = false;
}

void MostrarMatriz(int Mapa[][columnas])
{
	for (int i = 0; i < filas; i++)
	{
		for (int j = 0; j < columnas; j++)
		{
			Console::SetCursorPosition(0 + j, 0 + i);
			switch (Mapa[i][j])
			{
			case 0:
				cout << " ";
				break;
			case 1:
				cout << (char)219;
				break;
			}
		}
	}
}

struct Personaje
{
	int posX;
	int posY;
	int dx;
	int dy;
	char forma;
	int color;
};

void BorrarMovil(Personaje personaje)
{
	Console::SetCursorPosition(personaje.posX, personaje.posY); cout << " ";
}

void MoverMovil(Personaje& personaje, int Mapa[][columnas])
{
	char tecla;
	if (_kbhit())
	{
		tecla = _getch();
		switch (tecla)
		{
		case 72:
			if (Mapa[personaje.posY - personaje.dy][personaje.posX] == 0)
			{
				Mapa[personaje.posY][personaje.posX] = 1;
				MostrarMatriz(Mapa);
				personaje.posY -= personaje.dy;
			}
			break;
		case 80:
			if (Mapa[personaje.posY + personaje.dy][personaje.posX] == 0)
			{
				Mapa[personaje.posY][personaje.posX] = 1;
				MostrarMatriz(Mapa);
				personaje.posY += personaje.dy;
			}
			break;
		case 77:
			if (Mapa[personaje.posY][personaje.posX + personaje.dx] == 0)
			{
				Mapa[personaje.posY][personaje.posX] = 1;
				MostrarMatriz(Mapa);
				personaje.posX += personaje.dx;
			}
			break;
		case 75:
			if (Mapa[personaje.posY][personaje.posX - personaje.dx] == 0)
			{
				Mapa[personaje.posY][personaje.posX] = 1;
				MostrarMatriz(Mapa);
				personaje.posX -= personaje.dx;
			}
			break;
		}
	}
}

void DibujarMovil(Personaje personaje)
{
	Console::ForegroundColor = ConsoleColor(personaje.color);
	Console::SetCursorPosition(personaje.posX, personaje.posY); cout << personaje.forma;
	Console::ResetColor();
}

void DesplazarMovil(Personaje& personaje, int Mapa[][columnas])
{
	BorrarMovil(personaje);
	MoverMovil(personaje, Mapa);
	DibujarMovil(personaje);
	_sleep(1);
}

